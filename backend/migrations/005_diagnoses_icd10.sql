-- Phase 6: Tashxislar / ICD-10 Tables
-- Creates diagnoses and icd10 tables for diagnosis management

-- ===== ICD-10 REFERENCE TABLE =====
CREATE TABLE IF NOT EXISTS icd10 (
    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    code VARCHAR(20) NOT NULL,
    name VARCHAR(500) NOT NULL,
    parent_id UUID REFERENCES icd10(id) ON DELETE SET NULL,
    is_active BOOLEAN DEFAULT true,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX IF NOT EXISTS idx_icd10_code ON icd10(code);
CREATE INDEX IF NOT EXISTS idx_icd10_name ON icd10(name);
CREATE INDEX IF NOT EXISTS idx_icd10_name_gin ON icd10 USING gin(to_tsvector('russian', name));

-- ===== EPISODE DIAGNOSES TABLE =====
CREATE TABLE IF NOT EXISTS diagnoses (
    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    episode_id UUID NOT NULL REFERENCES episodes(id) ON DELETE CASCADE,
    icd_code VARCHAR(20) NOT NULL,
    icd_name VARCHAR(500) NOT NULL,
    type VARCHAR(50) NOT NULL DEFAULT 'main' CHECK (type IN ('main', 'secondary', 'complication')),
    status VARCHAR(50) NOT NULL DEFAULT 'preliminary' CHECK (status IN ('preliminary', 'confirmed', 'ruled_out')),
    notes TEXT,
    branch_id UUID REFERENCES branches(id) ON DELETE SET NULL,
    created_by UUID REFERENCES users(id),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_by UUID REFERENCES users(id)
);

CREATE INDEX IF NOT EXISTS idx_diagnoses_episode_id ON diagnoses(episode_id);
CREATE INDEX IF NOT EXISTS idx_diagnoses_icd_code ON diagnoses(icd_code);

